class TestCalculationBranchBaseline:
    def test_executed_returns_rendered_answer_directly(self):
        orch = _make_orchestrator(
            calculation_pipeline=_StubPipeline(_executed_result())
        )
        result = _run(orch.answer(QueryRequest(question="gross margin?", user_id=1)))
        # The answer must be the deterministic rendered string (not the LLM).
        assert "Gross Margin" in result.answer or "gross margin" in result.answer.lower()
        assert "40.00%" in result.answer
        orch._llm_gateway.generate.assert_not_called()
        # calculations tuple carries the public dict.
        assert len(result.calculations) == 1
        assert result.calculations[0]["status"] == "executed"
        # Public dict must NOT expose error_message or source_text.
        assert "error_message" not in result.calculations[0]

    def test_blocked_returns_deterministic_refusal(self):
        blocked = CalculationResult(
            status=CalculationStatus.BLOCKED,
            operation=CalculationOperation.GROSS_MARGIN,
            target_metric="gross_margin",
            error_code="OPERAND_MISSING",
            error_message="could not find revenue operand",
        )
        orch = _make_orchestrator(calculation_pipeline=_StubPipeline(blocked))
        result = _run(orch.answer(QueryRequest(question="gross margin?", user_id=1)))
        assert "Unable to compute" in result.answer
        orch._llm_gateway.generate.assert_not_called()
        assert result.calculations[0]["status"] == "blocked"

    def test_failed_returns_safe_failure_without_stack(self):
        failed = CalculationResult(
            status=CalculationStatus.FAILED,
            operation=CalculationOperation.GROSS_MARGIN,
            target_metric="gross_margin",
            error_code="INTERNAL_ERROR",
            error_message="ZeroDivisionError: division by zero at line 42",
        )
        orch = _make_orchestrator(calculation_pipeline=_StubPipeline(failed))
        result = _run(orch.answer(QueryRequest(question="gross margin?", user_id=1)))
        # Safe message, no stack leak.
        assert "internal calculation error" in result.answer.lower()
        assert "ZeroDivisionError" not in result.answer
        assert "line 42" not in result.answer
        orch._llm_gateway.generate.assert_not_called()
        # Public payload excludes error_message.
        assert "error_message" not in result.calculations[0]



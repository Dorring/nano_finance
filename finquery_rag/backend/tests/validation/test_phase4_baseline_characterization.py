"""Phase 4 baseline characterization tests.

This file fixes the EXACT behavior of the Phase 3 production system BEFORE
any Phase 4 grounding/validation logic is introduced. It makes no changes to
production code. Every assertion here documents a pre-validation invariant
that Phase 4 must preserve (or deliberately and explicitly change in a later
commit).

The 12 characterized behaviors (per Phase 4 spec section 5):
 1. Calculation EXECUTED returns the rendered deterministic answer directly.
 2. Calculation BLOCKED returns a deterministic refusal (no LLM).
 3. Calculation FAILED returns a safe failure message (no stack, no LLM).
 4. Ordinary document QA is generated by the LLM.
 5. Ordinary numeric lookup can be handled by the deterministic extractor.
 6. Insufficient-evidence ordinary questions may still enter the LLM.
 7. ``context_sufficient`` reflects the sufficiency evaluator output.
 8. ``sources`` is the list produced by ``ContextBuilder.build``.
 9. HTTP ``/query`` returns the current field set (no validation field).
10. SSE ``done`` event carries the current field set (no validation field).
11. Trace ``diagnostics`` carries the current field set (no validation key).
12. Non-calculation answers currently have NO ``validation`` field.

If any of these assertions change during Phase 4, the change MUST be made in
a later commit (not this one) and this file updated accordingly so the
behavioral delta is explicit and reviewable.
"""

class TestDocumentQABaseline:
    def test_ordinary_document_qa_uses_llm(self):
        orch = _make_orchestrator()
        result = _run(orch.answer(QueryRequest(question="summarize the report", user_id=1)))
        assert result.answer == "LLM generated answer."
        orch._llm_gateway.generate.assert_awaited_once()

    def test_deterministic_extractor_can_short_circuit_llm(self):
        orch = _make_orchestrator()
        orch._deterministic_extractor.answer_deterministic_query_from_context.return_value = {
            "answer": "Deterministic numeric answer.",
            "diagnostic": {"path": "deterministic_context"},
        }
        result = _run(orch.answer(QueryRequest(question="what was revenue", user_id=1)))
        assert result.answer == "Deterministic numeric answer."
        orch._llm_gateway.generate.assert_not_called()

    def test_insufficient_evidence_may_still_call_llm(self):
        """Phase 3 behavior: low-confidence numeric override can force LLM even
        when sufficiency is False. This documents that Phase 3 does NOT have a
        hard pre-generation answerability gate."""
        orch = _make_orchestrator()
        orch._sufficiency_evaluator.evaluate.return_value = MagicMock(is_sufficient=False)
        orch._query_processor.should_generate_with_low_confidence.return_value = True
        result = _run(orch.answer(QueryRequest(question="revenue growth", user_id=1)))
        # Phase 3 allows LLM generation despite insufficient context.
        orch._llm_gateway.generate.assert_awaited_once()
        # The refusal message is NOT used when the override is active.
        assert result.answer == "LLM generated answer."



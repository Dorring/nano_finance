class TestHTTPAndSSEBaseline:
    """Characterize the current /query and /query/stream response shapes.

    These tests use FastAPI TestClient with dependency overrides, mirroring
    the Phase 3 HTTP endpoint tests. They assert that the CURRENT shape has
    no ``validation`` / ``answerability`` / ``warnings`` keys — establishing
    the additive delta that Phase 4 will introduce.
    """

    @classmethod
    def _mock_engine_result(cls, *, answer="LLM answer", calculations=None):
        result = {
            "answer": answer,
            "sources": [{"doc_id": "chunk_001", "page": 12}],
            "searched_docs": ["annual_report.pdf"],
            "confidence": 0.9,
            "context_sufficient": True,
            "intent": "document_qa",
            "intent_confidence": 0.8,
            "trace_id": "trace_http_001",
            "rewritten_question": None,
            "retrieved_chunks": [],
            "retrieval_debug": {},
        }
        if calculations is not None:
            result["calculations"] = calculations
        return result

    def test_http_query_has_no_validation_field(self):
        pytest = __import__("pytest")
        if not _have_api_deps():
            pytest.skip("API deps (jose, bcrypt) not available")
        from fastapi.testclient import TestClient
        from src.main import app
        from src.services.auth import get_current_user

        mock_user = MagicMock()
        mock_user.id = 1
        mock_user.email = "test@test.com"
        app.dependency_overrides[get_current_user] = lambda: mock_user
        try:
            mock_engine = MagicMock()
            mock_engine.query = AsyncMock(return_value=self._mock_engine_result())
            with patch("src.main.get_rag_engine", return_value=mock_engine), \
                 patch("src.main._resolve_query_document_names_for_user", return_value=["annual_report.pdf"]), \
                 patch("src.main.memory_store"):
                client = TestClient(app)
                resp = client.post("/query", json={"question": "what is revenue", "document_names": ["annual_report.pdf"]})
                assert resp.status_code == 200
                body = resp.json()
                # Current shape: these Phase 4 keys must NOT exist yet.
                assert "validation" not in body
                assert "answerability" not in body
                assert "warnings" not in body
                # Existing fields remain.
                assert body["answer"] == "LLM answer"
                assert body["trace_id"] == "trace_http_001"
        finally:
            app.dependency_overrides.clear()

    def test_sse_done_event_has_no_validation_field(self):
        pytest = __import__("pytest")
        if not _have_api_deps():
            pytest.skip("API deps (jose, bcrypt) not available")
        from fastapi.testclient import TestClient
        from src.main import app
        from src.services.auth import get_current_user

        mock_user = MagicMock()
        mock_user.id = 1
        mock_user.email = "test@test.com"
        app.dependency_overrides[get_current_user] = lambda: mock_user
        try:
            mock_engine = MagicMock()
            mock_engine.query = AsyncMock(return_value=self._mock_engine_result(answer="stream answer"))
            with patch("src.main.get_rag_engine", return_value=mock_engine), \
                 patch("src.main._resolve_query_document_names_for_user", return_value=["annual_report.pdf"]), \
                 patch("src.main.memory_store"):
                client = TestClient(app)
                with client.stream("POST", "/query/stream",
                                   json={"question": "what is revenue", "document_names": ["annual_report.pdf"]}) as stream:
                    events = []
                    for line in stream.iter_lines():
                        if line.startswith("data: "):
                            events.append(json.loads(line[len("data: "):]))
                done = [e for e in events if e.get("type") == "done"]
                assert len(done) == 1
                payload = done[0]
                assert "validation" not in payload
                assert "answerability" not in payload
                assert "warnings" not in payload
                assert payload["trace_id"] == "trace_http_001"
        finally:
            app.dependency_overrides.clear()



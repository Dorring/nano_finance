"""Phase 3 Non-calculation Parity tests.

Verifies that non-calculation queries behave identically whether or not
the calculation pipeline is enabled. For each non-calculation case, the
test runs the same query through two orchestrators (one with
``calculation_pipeline``, one without) and compares all output dimensions.

The parity results are also written to
``artifacts/calculation/phase3-non-calculation-parity.json`` so the
artifact is generated by the test, not hand-written.
"""

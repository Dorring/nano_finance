PARITY_CASES = []


class TestNonCalculationParity:
    """Verify non-calculation queries behave identically with/without pipeline."""

    def _run_case(self, case_id, question, intent, chunks=None, llm_answer="LLM answer",
                  front_matter_answer=None, deterministic_context_answer=None,
                  list_docs=None, doc_names=("annual_report.pdf",), history=None):
        """Run a single parity case and return the comparison dict."""
        orch_with, llm_with, trace_with = _make_orchestrator(
            with_pipeline=True,
            intent=intent,
            chunks=chunks,
            llm_answer=llm_answer,
            front_matter_answer=front_matter_answer,
            deterministic_context_answer=deterministic_context_answer,
            list_docs=list_docs,
        )
        orch_without, llm_without, trace_without = _make_orchestrator(
            with_pipeline=False,
            intent=intent,
            chunks=chunks,
            llm_answer=llm_answer,
            front_matter_answer=front_matter_answer,
            deterministic_context_answer=deterministic_context_answer,
            list_docs=list_docs,
        )
        result_with = _run(orch_with.answer(_request(question, doc_names, history)))
        result_without = _run(orch_without.answer(_request(question, doc_names, history)))
        parity = _compare_parity(result_with, result_without, trace_with, trace_without)
        parity["case_id"] = case_id
        PARITY_CASES.append(parity)
        return parity, result_with, result_without

    def test_ordinary_document_qa(self):
        """Case 1: ordinary document QA (non-calculation)."""
        parity, rw, ro = self._run_case(
            case_id="ordinary_document_qa",
            question="What is the main business of the company?",
            intent={"intent": "document_qa", "requires_retrieval": True, "confidence": 0.9},
            chunks=[_chunk("The company manufactures electronic components and sells them globally.")],
            llm_answer="The company manufactures electronic components.",
        )
        assert all(v for k, v in parity.items() if k != "case_id")
        assert "calculations" not in rw.to_legacy_dict()

    def test_direct_numeric_query(self):
        """Case 2: direct numeric query (asking for a value, not a calculation)."""
        parity, rw, ro = self._run_case(
            case_id="direct_numeric_query",
            question="What was the total revenue in FY2025?",
            intent={"intent": "numeric_lookup", "requires_retrieval": True, "confidence": 0.9},
            chunks=[_chunk("Total revenue for FY2025 was $1,000,000.")],
            llm_answer="The total revenue in FY2025 was $1,000,000.",
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_front_matter_title(self):
        """Case 3: front-matter title query."""
        parity, rw, ro = self._run_case(
            case_id="front_matter_title",
            question="What is the title of this document?",
            intent={"intent": "title_query", "requires_retrieval": True, "confidence": 0.9},
            chunks=[_chunk("Annual Report 2025", page=1)],
            front_matter_answer={
                "answer": "Annual Report 2025",
                "chunks": [_chunk("Annual Report 2025", page=1)],
                "diagnostic": {"path": "front_matter", "field": "title"},
            },
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_document_summary(self):
        """Case 4: document summary request."""
        parity, rw, ro = self._run_case(
            case_id="document_summary",
            question="Summarize this report.",
            intent={"intent": "summary", "requires_retrieval": True, "confidence": 0.9},
            chunks=[_chunk("The report covers financial performance, market analysis, and future outlook for the fiscal year.")],
            llm_answer="This report covers financial performance and market analysis.",
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_conversation(self):
        """Case 5: conversational query (no retrieval needed)."""
        parity, rw, ro = self._run_case(
            case_id="conversation",
            question="Thank you for the information.",
            intent={"intent": "conversation", "requires_retrieval": False, "confidence": 0.9},
            chunks=[],
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_no_documents(self):
        """Case 6: no documents available."""
        parity, rw, ro = self._run_case(
            case_id="no_documents",
            question="What is the revenue?",
            intent={"intent": "document_qa", "requires_retrieval": True, "confidence": 0.9},
            chunks=[],
            list_docs=[],
            doc_names=(),
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_multi_document_query(self):
        """Case 7: multi-document query (non-calculation)."""
        parity, rw, ro = self._run_case(
            case_id="multi_document_query",
            question="Compare the business models across these reports.",
            intent={"intent": "document_qa", "requires_retrieval": True, "confidence": 0.9},
            chunks=[
                _chunk("Company A focuses on hardware.", chunk_id="c1", document_name="doc_a.pdf"),
                _chunk("Company B focuses on software.", chunk_id="c2", document_name="doc_b.pdf"),
            ],
            llm_answer="Company A focuses on hardware, Company B on software.",
            doc_names=("doc_a.pdf", "doc_b.pdf"),
        )
        assert all(v for k, v in parity.items() if k != "case_id")

    def test_insufficient_evidence(self):
        """Case 8: insufficient evidence for a non-calculation question."""
        parity, rw, ro = self._run_case(
            case_id="insufficient_evidence_non_calc",
            question="What are the risk factors?",
            intent={"intent": "document_qa", "requires_retrieval": True, "confidence": 0.9},
            chunks=[_chunk("Some unrelated content about weather.")],
            llm_answer="I couldn't find sufficiently relevant information.",
        )
        assert all(v for k, v in parity.items() if k != "case_id")


class TestNonCalculationParityArtifact:
    """Generate the parity JSON artifact from collected test results."""

    def test_generate_parity_artifact(self, tmp_path=None):
        """Write parity results to artifacts/calculation/phase3-non-calculation-parity.json.

        This test runs after all parity cases have been collected.
        If no cases were collected (standalone run), it runs all 8 cases.
        """
        cases = list(PARITY_CASES)
        if not cases:
            # Standalone run: execute all 8 cases
            runner = TestNonCalculationParity()
            runner.test_ordinary_document_qa()
            runner.test_direct_numeric_query()
            runner.test_front_matter_title()
            runner.test_document_summary()
            runner.test_conversation()
            runner.test_no_documents()
            runner.test_multi_document_query()
            runner.test_insufficient_evidence()
            cases = list(PARITY_CASES)

        # Build artifact
        artifact_dir = Path(__file__).parent.parent.parent / "artifacts" / "calculation"
        artifact_dir.mkdir(parents=True, exist_ok=True)
        artifact_path = artifact_dir / "phase3-non-calculation-parity.json"

        # Normalize field order per the spec
        ordered_cases = []
        for c in cases:
            ordered_cases.append({
                "case_id": c["case_id"],
                "query_expansion_equal": c["query_expansion_equal"],
                "retrieval_order_equal": c["retrieval_order_equal"],
                "context_equal": c["context_equal"],
                "prompt_equal": c["prompt_equal"],
                "ordinary_answer_equal": c["ordinary_answer_equal"],
                "sources_equal": c["sources_equal"],
                "legacy_fields_equal": c["legacy_fields_equal"],
                "trace_existing_fields_equal": c["trace_existing_fields_equal"],
            })

        artifact_path.write_text(
            json.dumps(ordered_cases, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )

        # Assert all parity dimensions are true
        for case in ordered_cases:
            for key, val in case.items():
                if key == "case_id":
                    continue
                assert val is True, f"{case['case_id']}.{key} = {val}"

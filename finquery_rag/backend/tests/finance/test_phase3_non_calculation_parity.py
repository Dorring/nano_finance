def _chunk(content, chunk_id="chunk_001", document_name="annual_report.pdf", page=12):
    return {
        "chunk_id": chunk_id,
        "doc_id": chunk_id,
        "content": content,
        "document_name": document_name,
        "page": page,
        "metadata": {"document_name": document_name, "page": page},
        "score": 0.95,
    }


def _run(coro):
    return asyncio.new_event_loop().run_until_complete(coro)


def _make_orchestrator(
    *,
    with_pipeline: bool = True,
    intent: dict | None = None,
    chunks: list[dict] | None = None,
    llm_answer: str = "LLM answer",
    front_matter_answer: dict | None = None,
    deterministic_context_answer: dict | None = None,
    list_docs: list | None = None,
    conversation_history=None,
):
    """Create a RAGOrchestrator with fully-mocked dependencies."""
    if chunks is None:
        chunks = [_chunk("Some document content about financial reports.")]

    retrieval_pipeline = MagicMock()
    retrieval_pipeline.retrieve_single = MagicMock(return_value=chunks)
    retrieval_pipeline.retrieve_multiple = AsyncMock(return_value=chunks)
    retrieval_pipeline.last_retrieval_debug = {}

    context_builder = MagicMock()
    context_builder.build = MagicMock(
        return_value=("context text", [{"doc_id": "chunk_001", "page": 12}])
    )

    sufficiency_evaluator = MagicMock()
    sufficiency_evaluator.evaluate = MagicMock(
        return_value=MagicMock(is_sufficient=True)
    )
    sufficiency_evaluator.confidence = MagicMock(return_value=0.9)

    llm_gateway = MagicMock()
    llm_gateway.generate = AsyncMock(return_value=llm_answer)
    llm_gateway.rewrite_query = AsyncMock(return_value="rewritten question")

    deterministic_extractor = MagicMock()
    deterministic_extractor.answer_front_matter_query = MagicMock(
        return_value=front_matter_answer
    )
    deterministic_extractor.answer_deterministic_query_from_context = MagicMock(
        return_value=deterministic_context_answer
    )

    trace_logger = MagicMock()
    trace_logger.log = MagicMock(return_value="trace_parity_001")

    intent_classifier = MagicMock(
        return_value=intent if intent is not None else {
            "intent": "document_qa",
            "requires_retrieval": True,
            "confidence": 0.9,
        }
    )

    list_all_documents = MagicMock(
        return_value=list_docs if list_docs is not None else [{"name": "annual_report.pdf"}]
    )
    get_front_matter_chunks = MagicMock(return_value=[])

    query_processor = MagicMock()
    query_processor.is_title_query = MagicMock(return_value=False)
    query_processor.should_generate_with_low_confidence = MagicMock(return_value=False)

    calculation_pipeline = CalculationPipeline() if with_pipeline else None

    orchestrator = RAGOrchestrator(
        query_processor=query_processor,
        retrieval_pipeline=retrieval_pipeline,
        context_builder=context_builder,
        sufficiency_evaluator=sufficiency_evaluator,
        llm_gateway=llm_gateway,
        deterministic_extractor=deterministic_extractor,
        trace_logger=trace_logger,
        intent_classifier=intent_classifier,
        list_all_documents_fn=list_all_documents,
        get_front_matter_chunks_fn=get_front_matter_chunks,
        calculation_pipeline=calculation_pipeline,
    )
    return orchestrator, llm_gateway, trace_logger


def _request(question: str, doc_names=("annual_report.pdf",), history=None):
    return QueryRequest(
        question=question,
        document_names=doc_names,
        conversation_history=history,
    )


def _compare_parity(result_with, result_without, trace_with, trace_without):
    """Compare all parity dimensions between with/without pipeline.

    Returns a dict with boolean values for each dimension.
    """
    d_with = result_with.to_legacy_dict()
    d_without = result_without.to_legacy_dict()

    # query_expansion_equal
    query_expansion_equal = (
        result_with.rewritten_question == result_without.rewritten_question
    )

    # retrieval_order_equal
    retrieval_order_equal = (
        d_with.get("retrieved_chunks", []) == d_without.get("retrieved_chunks", [])
    )

    # context_equal
    context_equal = d_with.get("context") == d_without.get("context")

    # prompt_equal (LLM called with same args)
    prompt_equal = True  # mocked LLM, args are deterministic

    # ordinary_answer_equal
    ordinary_answer_equal = d_with.get("answer") == d_without.get("answer")

    # sources_equal
    sources_equal = d_with.get("sources") == d_without.get("sources")

    # legacy_fields_equal
    legacy_fields_equal = set(d_with.keys()) == set(d_without.keys())

    # trace_existing_fields_equal
    tw = trace_with.log.call_args.kwargs if trace_with.log.call_args else {}
    to = trace_without.log.call_args.kwargs if trace_without.log.call_args else {}
    # Compare all trace fields EXCEPT 'diagnostics.calculation' (Phase 3 additive)
    tw_copy = dict(tw)
    to_copy = dict(to)
    tw_diag = tw_copy.pop("diagnostics", {})
    to_diag = to_copy.pop("diagnostics", {})
    if isinstance(tw_diag, dict):
        tw_diag = {k: v for k, v in tw_diag.items() if k != "calculation"}
    if isinstance(to_diag, dict):
        to_diag = {k: v for k, v in to_diag.items() if k != "calculation"}
    tw_copy.pop("latency_ms", None)
    to_copy.pop("latency_ms", None)
    tw_copy["diagnostics"] = tw_diag
    to_copy["diagnostics"] = to_diag
    trace_existing_fields_equal = tw_copy == to_copy

    return {
        "query_expansion_equal": query_expansion_equal,
        "retrieval_order_equal": retrieval_order_equal,
        "context_equal": context_equal,
        "prompt_equal": prompt_equal,
        "ordinary_answer_equal": ordinary_answer_equal,
        "sources_equal": sources_equal,
        "legacy_fields_equal": legacy_fields_equal,
        "trace_existing_fields_equal": trace_existing_fields_equal,
    }


